    /**
     * Handle the user "updated" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function updated(User $user)
    {
        if ($user->wasChanged('email_verified_at')) return;
        if($user->wasChanged([
            'name',
            'email',
            'password',
            'cpf',
            'rg',
            'rg_emmitter',
            'social_name',
            'mother_name',
            'birth_date'
        ])) return $user->notify(new UpdateUser());
    }

